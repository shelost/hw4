for (let i=0; i<ELEMS.length; i++){
    let elem = ELEMS[i]

    let string = ``

    switch (elem.type){

        // HEADER
        case "header":
            string +=
        `
        <div class = 'header' id = '${elem.title}'>
            <h1> ${elem.title} </h1>
            <h2> ${elem.subtitle} </h2>
        </div>
        `
        break;

        // ITEM
        case "item":
            string +=
            `
            <div class = 'item
            `

            for (let j=0; j<elem.class.length; j++){
                string += ` ${elem.class[j]} `
            }

            string +=
            `
                '>
                <div style='background-image: url(Assets/${elem.img}.png)' class = 'img
            `

            if (elem.stroke){
                string += `stroke`
            }

            string +=
            `
                '>
                </div>
                <div class = 'expo'>
                    <h1> ${elem.title} </h1>
                    <h2> ${elem.subtitle} </h2>
                    <p> ${elem.blurb} </p>
                    <div class = 'flex'>
            `

            for (let j=0; j<elem.links.length; j++){
                let link = elem.links[j]

                if (link.title != '/'){
                    string +=
                    `
                    <a href = '${link.href}'>
                        <h3> ${link.title} </h3>
                    </a>
                    `
                }else{
                    string +=
                    `
                        <h3 class = 'slash'> / </h3>
                    `
                }

            }

            string +=
            `
            </div> </div> </div>
            `
        break;

        // LIST
        case "list":
            string +=
            `
            <div class = 'list'>
            `

            for (let j=0; j<elem.entries.length; j++){
                let entry = elem.entries[j]

                switch(entry.type){

                    // DATE LIST
                    case "date":
                        string +=
                        `
                        <div class = 'entry'>
                            <h1 class = 'date'> ${entry.date}: </h1>
                            <h1> ${entry.text} </h1>
                        </div>
                        `
                    break;

                    default:
                    break;
                }

            }

            string +=
            `
            </div>
            `
        break;

        // CAROUSEL
        case "carousel":
            string +=
            `
            <div class = 'carousel'>
            `

            for (let j=0; j<elem.entries.length; j++){
                let entry = elem.entries[j]

                switch(entry.type){

                    // DATE LIST
                    case "box":
                        string +=
                        `
                        <a href = '${entry.href}'>
                        <div class = 'box' style = 'background-image: url(Assets/${entry.img}.png)'>
                        </div>
                        </a>
                        `
                    break;

                    default:
                    break;
                }
            }

            string +=
            `
            </div>
            `
        break;

        default:
        break;
    }

    document.body.innerHTML += string
}




